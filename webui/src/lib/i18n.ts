let initialized = false;

export function initI18n(): void {
	if (initialized) return;
	const initialLocale = readInitialLocale();
	for (const lang of locales) {
		addMessages(lang, dictionaries[lang]);
	}
	init({
		fallbackLocale: defaultLocale,
		initialLocale,
	});
	applyDocumentLocale(initialLocale);
	initialized = true;
}

export function setAppLocale(value: AppLocale): void {
	if (browser) localStorage.setItem(localeStorageKey, value);
	locale.set(value);
	applyDocumentLocale(value);
}

function applyDocumentLocale(value: AppLocale): void {
	if (!browser) return;
	document.documentElement.dir = value === 'ar' ? 'rtl' : 'ltr';
	document.documentElement.lang = value;
}

function readInitialLocale(): AppLocale {
	if (!browser) return defaultLocale;
	const saved = localStorage.getItem(localeStorageKey);
	return isAppLocale(saved) ? saved : defaultLocale;
}

function isAppLocale(value: string | null): value is AppLocale {
	return !!value && (locales as readonly string[]).includes(value);
}

function mapMessages(base: Messages, overrides: PartialDeep<Messages>): Messages {
	return deepMerge(base, overrides);
}

type PartialDeep<T> = {
	[K in keyof T]?: T[K] extends Record<string, unknown> ? PartialDeep<T[K]> : T[K];
};

function deepMerge(base: Messages, overrides: PartialDeep<Messages>): Messages {
	const out: Messages = { ...base };
	for (const key of Object.keys(overrides)) {
		const value = overrides[key];
		if (isPlainRecord(out[key]) && isPlainRecord(value)) {
			out[key] = deepMerge(out[key] as Messages, value as PartialDeep<Messages>);
		} else if (value !== undefined) {
			out[key] = value as string | MessageTree;
		}
	}
	return out;
}

function isPlainRecord(value: unknown): value is Record<string, unknown> {
	return typeof value === 'object' && value !== null && !Array.isArray(value);
}
